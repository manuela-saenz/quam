        del consentimiento puede darse. La primera, puede ser sobre la totalidad de las

        finalidades consentidas, esto es, que QUAM, deba dejar de

        tratar por completo los datos del titular; la segunda, puede ocurrir sobre tipos de

        tratamiento determinados, como por ejemplo para fines publicitarios o de estudios

        de mercado. Con la segunda modalidad, esto es, la revocación parcial del

        consentimiento, se mantienen a salvo otros fines del tratamientos que el

        responsable, de conformidad con la autorización otorgada puede llevar a cabo y

        con los que el titular está de acuerdo.

