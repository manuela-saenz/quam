      <h3>4. Principios específicos</h3>

      <p>

        <strong>QUAM</strong> aplicará los siguientes principios específicos que se

        establecen a continuación, los cuales constituyen las reglas a seguir en la

        recolección, manejo, uso, tratamiento, almacenamiento e intercambio, de datos

        personales:

      </p>

      <ol type="a">

        <li>Principio de legalidad: En el uso, captura, recolección y tratamiento de

          datos personales, se dará aplicación a las disposiciones vigentes y

          aplicables que rigen el tratamiento de datos personales y demás derechos

          fundamentales conexos.</li>

        <li>

          Principio de libertad: El, uso, captura, recolección y tratamiento de datos

          personales sólo puede llevarse a cabo con el consentimiento, previo,

          expreso e informado del Titular. Los datos personales no podrán ser

          obtenidos o divulgados sin previa autorización, o en ausencia de mandato

          legal, estatutario, o judicial que releve el consentimiento.

        </li>

        <li>

          Principio de finalidad: El uso, captura, recolección y tratamiento de datos

          personales a los que tenga acceso y sean acopiados y recogidos por

          <strong>QUAM</strong> estarán subordinados y atenderán una finalidad

          legítima, la cual debe serle informada al respectivo titular de los datos

          personales.

        </li>

        <li>

          Principio de veracidad o calidad: La información sujeta a uso, captura,

          recolección y tratamiento de datos personales debe ser veraz, completa,

          exacta, actualizada, comprobable y comprensible. Se prohíbe el

          Tratamiento de datos parciales, incompletos, fraccionados o que induzcan a

          error.

        </li>

        <li>

          Principio de transparencia: En el uso, captura, recolección y tratamiento de

          datos personales debe garantizarse el derecho del Titular a obtener de

          <strong>QUAM</strong>, en cualquier momento y sin restricciones,

          información acerca de la existencia de cualquier tipo de información o dato

          personal que sea de su interés o titularidad.

        </li>

        <li>

          Principio de acceso y circulación restringida: Los datos personales, salvo la

          información pública, no podrán estar disponibles en Internet u otros medios

          de divulgación o comunicación masiva, salvo que el acceso sea

          técnicamente controlable para brindar un conocimiento restringido sólo a los

          Titulares o terceros autorizados. Para estos propósitos la obligación de

          <strong>QUAM</strong>, será de medio.

        </li>



        <li>

          Principio de seguridad: Los datos personales e información usada,

          capturada, recolectada y sujeta a tratamiento por <strong>QUAM</strong>,

          será objeto de protección en la medida en que los recursos técnicos y

          estándares mínimos así lo permitan, a través de la adopción de medidas

          tecnológicas de protección, protocolos, y todo tipo de medidas

          administrativas que sean necesarias para otorgar seguridad a los registros

          y repositorios electrónicos evitando su adulteración, modificación, pérdida,

          consulta, y en general en contra de cualquier uso o acceso no autorizado.

        </li>

        <li>

          Principio de confidencialidad: Todas y cada una de las personas que

          administran, manejen, actualicen o tengan acceso a informaciones de

          cualquier tipo que se encuentre en Bases o Bancos de Datos, se

          comprometen a conservar y mantener de manera estrictamente confidencial

          y no revelarla a terceros, todas las informaciones personales, comerciales,

          contables, técnicas, comerciales o de cualquier otro tipo suministradas en la

          ejecución y ejercicio de sus funciones. Todas las personas que trabajen

          actualmente o sean vinculadas a futuro para tal efecto, en la administración

          y manejo de bases de datos, deberán suscribir un documento adicional u

          otrosí a su contrato laboral o de prestación de servicios para efectos de

          asegurar tal compromiso. Esta obligación persiste y se mantiene inclusive

          después de finalizada su relación con alguna de las labores que comprende

          el Tratamiento.

        </li>

      </ol>

