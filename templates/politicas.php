    <div class="item">

      <h3>5. Autorización del titular:</h3>

      <p>

        Sin perjuicio de las excepciones previstas en la ley, en el tratamiento se requiere

        la autorización previa, expresa e informada del titular, la cual deberá ser obtenida

        por cualquier medio que pueda ser objeto de consulta y verificación posterior.

      </p>

    </div>

    <div class="item">

      <h3>5.1 Casos en que no se requiere la autorización:</h3>

      <p>

        La autorización del Titular no será necesaria cuando se trate de:

      </p>

      <ol type="a">

        <li>

          Información requerida por una entidad pública o administrativa en ejercicio

          de sus funciones legales o por orden judicial.

        </li>

        <li>

          Datos de naturaleza pública.

        </li>

        <li>

          Casos de urgencia médica o sanitaria.

        </li>

        <li>

          Tratamiento de información autorizado por la ley para fines históricos,

          estadísticos o científicos.

        </li>

        <li>

          Datos relacionados con el Registro Civil de las Personas.

        </li>

      </ol>

      <p>

        Para obtener la autorización del titular se deberá seguir las siguientes

        instrucciones: <br><br>

        Antes de que la persona autorice es necesario informarle de forma clara y

        expresa lo siguiente:

      </p>

      <ol>

        <li>

          El Tratamiento al cual serán sometidos sus datos personales y la

          finalidad del mismo.

        </li>

        <li>

          El carácter facultativo de las respuestas a las preguntas que le sean

          hechas, cuando estas versen sobre Datos Sensibles o sobre datos de las

          niñas, niños y adolescentes.

        </li>

        <li>

          Los derechos que le asisten como Titular previstos en el artículo 8 de la

          Ley 1581 de 2012 y que se encuentran descritos en el Sección 10 de este

          manual.

        </li>

        <li>

          La identificación, dirección física o electrónica y teléfono de <strong>QUAM</strong>

        </li>

      </ol>

      <p>

        La autorización también podrá obtenerse a partir de conductas inequívocas del

        Titular del Dato que permitan concluir de manera razonable que éste otorgó su

        consentimiento para el tratamiento de su información. Dicha(s) conducta(s)

        debe(n) ser muy clara(s) de manera que no admita(n) duda o equivocación sobre

        la voluntad de autorizar el Tratamiento y las finalidades del mismo. En ningún caso

        el silencio del Titular podrá considerarse como una conducta inequívoca.

      </p>

    </div>



    <div class="item">

      <h3>

        5.2 Dato sensible:

      </h3>

      <p>Se entiende por datos sensibles aquellos que afectan la intimidad del titular o cuyo

        uso indebido puede generar su discriminación, tales como aquellos que revelen el

        origen racial o étnico, la orientación política, las convicciones religiosas o

        filosóficas, la pertenencia a sindicatos, organizaciones sociales, de derechos

        humanos o que promueva intereses de cualquier partido político o que garanticen

        los derechos y garantías de partidos políticos de oposición así como los datos

        relativos a la salud, a la vida sexual y los datos biométricos, entre otros, la captura

        de imagen fija o en movimiento, huellas digitales, fotografías, iris, reconocimiento

        de voz, facial o de palma de mano, etc.</p>

    </div>



    <div class="item">

      <h3>

        5.2.1 Tratamiento de datos sensibles:

      </h3>



      <p>Se podrá hacer uso y tratamiento de los datos catalogados como sensibles

        cuando:</p>

      <li>El Titular haya dado su autorización explícita a dicho tratamiento, salvo en

        los casos que por ley no sea requerido el otorgamiento de dicha

        autorización.</li>

      <li>El Tratamiento sea necesario para salvaguardar el interés vital del titular y

        este se encuentre física o jurídicamente incapacitado. En estos eventos, los

        representantes legales deberán otorgar su autorización.</li>

      <li>El Tratamiento sea efectuado en el curso de las actividades legítimas y con

        las debidas garantías por parte de una fundación, ONG, asociación o

        cualquier otro organismo sin ánimo de lucro, cuya finalidad sea política,

        filosófica, religiosa o sindical, siempre que se refieran exclusivamente a sus

        miembros o a las personas que mantengan contactos regulares por razón

        de su finalidad. En estos eventos, los datos no se podrán suministrar a

        terceros sin la autorización del titular.</li>

      <li>El Tratamiento se refiera a datos que sean necesarios para el

        reconocimiento, ejercicio o defensa de un derecho en un proceso judicial.</li>

      <li>El Tratamiento tenga una finalidad histórica, estadística o científica. En este

        evento deberán adoptarse las medidas conducentes a la supresión de

        identidad de los Titulares.

        Autorización para el Tratamiento de Datos Sensibles:

        Cuando se trate de la recolección de datos Sensibles se cumplirá con los

        siguientes requisitos:</li>

      <ol type="a">

        <li>La autorización debe ser explícita.</li>

        <li>Se debe informar al Titular que no está obligado a autorizar el

          tratamiento de dicha autorización.</li>

        <li>Se debe informar de forma explícita y previa al Titular cuáles de los

          datos que serán objeto de Tratamiento son Sensibles y la finalidad del

          mismo.</li>

      </ol>

    </div>



    <div class="item">

      <h3>5.3 Autorización para el tratamiento de los niños, niñas y adolescentes.</h3>



      <p>

        En el Tratamiento se asegurará el respeto a los derechos prevalentes de los

        menores. Queda proscrito el Tratamiento de datos personales de menores, salvo

        aquellos datos que sean de naturaleza pública.

        Cuando se trate de la recolección y tratamiento de datos personales de niños,

        niñas y adolescentes, se deberá cumplir estrictamente con los siguientes

        requisitos:

      </p>



      <ol type="a">

        <li>La autorización debe ser otorgada por personas que estén facultadas para

          representar a los niños, niñas y adolescentes. El representante de los

          niños, niñas y adolescentes deberá garantizarles el derecho a ser

          escuchados y valorar su opinión del tratamiento teniendo en cuenta la

          madurez, autonomía y capacidad de los niños, niñas y adolescentes para

          entender el asunto.</li>

        <li>Se debe informar que es facultativo responder a las preguntas sobre

          Datos de los niños, niñas y adolescentes.</li>

        <li>El Tratamiento debe respetar el interés superior de los niños, niñas y

          adolescentes y asegurar el respeto de sus derechos fundamentales. Se

          debe informar explícita y previa al Titular cuáles de los Datos que serán

          objeto de tratamiento son Sensibles y la finalidad de los mismos.</li>

      </ol>



    </div>



    <div class="item">

      <h3>

        5.4 Clasificación y tratamiento especial de ciertos datos personales.

      </h3>

      <p>

        Las personas obligadas al cumplimiento de este manual deben identificar los

        Datos Sensibles y de los niños, niñas y adolescentes que eventualmente

        recolecten o almacenen con miras a:

      </p>



      <ol type="a">

        <li>Implementar responsabilidades reforzadas en el tratamiento de estos datos

          que se traduce en una exigencia mayor en términos de cumplimiento de los

          principios y deberes.</li>

        <li>Incrementar los niveles de seguridad de esa información.</li>

        <li>Aumentar las restricciones de acceso y uso por parte del personal no

          autorizado de <strong>QUAM</strong>, y de terceros.</li>

        <li>

          Tener siempre presente los requisitos legales y de este manual para su

          recolección.

        </li>

      </ol>



    </div>





    <div class="item">

      <h3>6. Deberes de QUAM como responsable del Tratamiento de

        Datos Personales.</h3>

      <p>

        <strong>QUAM</strong>, cuando actúe como Responsables del Tratamiento de

        datos personales, cumplirá con los siguientes deberes:

      </p>



      <ol type="a">

        <li>Garantizar al Titular, en todo tiempo, el pleno y efectivo ejercicio del

          derecho de hábeas data.</li>

        <li>Solicitar y conservar, copia de la respectiva autorización otorgada por el

          titular.</li>

        <li>Informar debidamente al titular sobre la finalidad de la recolección y los

          derechos que le asisten por virtud de la autorización otorgada.</li>

        <li>

          Conservar la información bajo las condiciones de seguridad necesarias

          para impedir su adulteración, pérdida, consulta, uso o acceso no autorizado

          o fraudulento.

        </li>

        <li>

          Garantizar que la información que se suministre al encargado del

          tratamiento sea veraz, completa, exacta, actualizada, comprobable y

          comprensible.

        </li>

        <li>

          Actualizar la información, comunicando de forma oportuna al encargado del

          tratamiento, todas las novedades respecto de los datos que previamente le

          haya suministrado y adoptar las demás medidas necesarias para que la

          información suministrada a este se mantenga actualizada.

        </li>

        <li>

          Rectificar la información cuando sea incorrecta y comunicar lo pertinente al

          encargado del tratamiento.

        </li>

        <li>

          Suministrar al Encargado del Tratamiento, según el caso, únicamente datos

          cuyo Tratamiento esté previamente autorizado.

        </li>

        <li>

          Exigir al Encargado del Tratamiento en todo momento, el respeto a las

          condiciones de seguridad y privacidad de la información del Titular.

        </li>

        <li>

          Tramitar las consultas y reclamos formulados.

        </li>

        <li>

          Informar al Encargado del Tratamiento cuando determinada información se

          encuentra en discusión por parte del Titular, una vez se haya presentado la

          reclamación y no haya finalizado el trámite respectivo.

        </li>

        <li>

          Informar a solicitud del Titular sobre el uso dado a sus datos.

        </li>

        <li>

          Informar a la autoridad de protección de datos cuando se presenten

          violaciones a los códigos de seguridad y existan riesgos en la

          administración de la información de los Titulares.

        </li>

      </ol>



    </div>



    <div class="item">

      <h3>7. Tratamientos de los datos personales y la finalidad del mismo.</h3>

      <p>

        <strong>QUAM</strong>, tratará de manera leal y lícita los datos personales

        para cumplir los fines relacionados con su objeto social y en especial:

      </p>



      <p><strong>a) Información del Recurso Humano:</strong></p>



      <p>

        <strong>Tratamiento de datos antes de la relación contractual:</strong> La compañía tratará los

        datos personales de sus empleados, así como respecto de aquellos que se

        postulen para vacantes, en tres momentos a saber: antes, durante y después de la

        relación laboral y/o de servicios.

        <br><br>

        Tratamiento antes de la relación laboral. <strong>QUAM</strong> informará, de

        manera anticipada, a las personas interesadas en participar en un proceso de

        selección, las reglas aplicables al tratamiento de los datos personales que

        suministre el interesado, así como respecto de aquellos que se obtengan durante

        el proceso de selección.

        <br><br>

        <strong>QUAM</strong>, una vez agote el proceso de selección, informará el

        resultado negativo a las personas no seleccionadas. La información obtenida por

        <strong>QUAM</strong> respecto de quienes no fueron seleccionados,

        resultados de las pruebas sicotécnicas y entrevistas, serán eliminados de sus

        sistemas de información.

        <br><br>

        Los datos personales e información obtenida del proceso de selección respecto

        del personal seleccionado para laborar en <strong>QUAM</strong> serán

        almacenados en la carpeta personal.

        <br><br>

        <strong>Tratamiento de datos durante la relación contractual.</strong>

        <strong>QUAM</strong> almacenará los datos personales e información personal

        obtenida del proceso de selección de los empleados en una carpeta identificada

        con el nombre de cada uno de ellos. Esta carpeta física solo será accedida y

        tratada con la finalidad de administrar la relación contractual entre <strong>QUAM</strong> y el

        empleado.

        <br><br>

        El uso de la información de los empleados para fines diferentes a la administración

        de la relación contractual, está prohibido en <strong>QUAM</strong>.

        <br><br>

        <strong>Tratamiento de datos después de terminada la relación contractual.</strong>

        <br><br>

        Terminada la relación laboral, cualquiera que fuere la causa, <strong>QUAM</strong> procederá a

        almacenar los datos personales obtenidos del proceso de

        selección y documentación generada en el desarrollo de la relación laboral, en un

        archivo central, sometiendo tal información a medidas y niveles de seguridad altas,

        en virtud de la potencialidad de que la información laboral pueda contener datos

        sensibles.

        <br><br>

        <strong>QUAM</strong>, en este archivo almacenará por el tiempo que las leyes

        para este fin establezcan los datos personales de todos sus ex-empleados. Este

        archivo tendrá como finalidad:



      <ol type="a">

        <li>Servir como base para la expedición de los certificados laborales de

          que trata el Artículo 57, numeral 7o del Código Sustantivo de Trabajo a

          solicitud de sus ex–empleados o sus causahabientes.</li>

        <li>

          Servir de base para el otorgamiento de referencias laborales a

          potenciales empleadores del ex -empleado, cuando éste así lo autorice

          al momento de su desvinculación de QUAM,

        </li>

        <li>

          La información almacenada en estos archivos podrá ser estudiada,

          analizada y utilizada por QUAM, para el fin de

          considerar al ex-empleado en los procesos de contratación de personal

          que lleve a cabo.

        </li>

      </ol>

      </p>



      <p><strong>b) Información de los Accionistas:</strong></p>



      <p>

        Tratamiento de datos personales de Accionistas: Los datos e

        información personal de las personas físicas que llegaren a tener la

        condición de accionista de QUAM, se considerará

        información reservada, pues la misma está registrada en los libros de

        comercio y tiene el carácter de reserva por disposición legal. <br>

        En consecuencia, el acceso a tal información personal se realizará

        conforme las normas contenidas en el Código de Comercio que regulan la

        materia. <br>

        QUAM solo usará los datos personales de los

        accionistas para las finalidades derivadas de la relación estatutaria

        existente.

      </p>

      <p><strong>c) Información del Cliente:</strong></p>



      <p>

        Tratamiento de datos personales de Clientes: <strong>QUAM</strong>

        podrá realizar el tratamiento de información personal acerca de sus

        clientes, solo para los propósitos de prestar sus servicios. La información

        personal de los clientes puede ser compartida internamente con el

        propósito de determinar el cumplimiento de los procedimientos aplicables,

        políticas internas de <strong>QUAM</strong>, o en revisiones de calidad.

