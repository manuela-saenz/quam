      <h3>3. Definiciones</h3>

      <p>

        De conformidad con la legislación vigente sobre la materia, se establecen las

        siguientes definiciones, las cuales serán aplicadas e implementadas acogiendo los

        criterios de interpretación que garanticen una aplicación sistemática e integral, y

        en consonancia con los avances tecnológicos, la neutralidad tecnológica; y los

        demás principios y postulados que rigen los derechos fundamentales que

        circundan, orbitan y rodean el derecho de habeas data y protección de datos

        personales.

        <br><br>

        <strong>Autorización:</strong> Consentimiento previo, expreso e informado del titular para llevar a

        cabo el Tratamiento de datos personales.



        <br><br>

        <strong>Base de datos:</strong> Conjunto organizado de datos personales que sea objeto de

        Tratamiento.



        <br><br>

        <strong>Dato personal:</strong> Cualquier información vinculada o que pueda asociarse a una o

        varias personas naturales determinadas o determinables.



        <br><br>

        <strong>Dato privado:</strong> Es un dato personal que por su naturaleza intima o reservada solo

        interesa a su titular y para su tratamiento requiere autorización expresa



        <br><br>

        <strong>Dato semiprivado:</strong> Son datos que no tienen naturaleza íntima, reservada ni

        pública y cuyo conocimiento o divulgación puede interesar no solo a su titular sino

        a un grupo de personas o a la sociedad en general



        <br><br>

        <strong>Encargado del tratamiento:</strong> Persona natural o jurídica, pública o privada, que por

        sí misma o en asocio con otros, realice el tratamiento de datos personales por

        cuenta del responsable del tratamiento.



        <br><br>

        <strong>Responsable del tratamiento:</strong> Persona natural o jurídica, pública o privada, que

        por sí misma o en asocio con otros, decida sobre la base de datos y/o el

        tratamiento de los datos.



        <br><br>

        <strong>Titular:</strong> Persona natural cuyos datos personales sean objeto de tratamiento.



        <br><br>

        <strong>Tratamiento:</strong> Cualquier operación o conjunto de operaciones sobre datos

        personales, tales como la recolección, almacenamiento, uso, circulación o

        supresión.

      </p>

